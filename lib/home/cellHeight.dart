double cellHeight(String type) {
  switch (type) {
    case "top":
      {
        return 218;
      }
      break;

    case "product":
      {
        return 246;
      }
      break;

    case "shop":
      {
        return 246;
      }
      break;

    default:
      {
        return 180;
      }
  }

}